SELECT
    SUM(CASE WHEN InvoiceDate BETWEEN '2009-01-01' AND '2009-12-31'THEN Total ELSE 0 END) as Sales_2009,
    SUM(CASE WHEN InvoiceDate BETWEEN '2011-01-01' AND '2011-12-31'THEN Total ELSE 0 END) as Sales_2011
    
FROM Invoice
WHERE (InvoiceDate BETWEEN '2009-01-01' AND '2009-12-31')
OR (InvoiceDate BETWEEN '2011-01-01' AND '2011-12-31')